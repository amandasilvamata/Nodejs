let diaEmIngles = `tuesday`

switch(diaEmIngles){
    case `monday`:
        console.log(`segunda-feira`)
        break
    case `tuesday`:
        console.log(`terça-feira`)
        break
    case `wednesday`:
        console.log(`quarta-feira`)
        break
    case `thursday`:
        console.log(`quinta-feira`)
        break
    case `friday`:
        console.log(`sexta-feira`)
    case `saturday`:
        console.log(`sábado`)
        break
    case `sunday`:
        console.log(`domingo`)
}